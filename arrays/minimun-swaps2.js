function minimumSwaps(arr) {
  let counter = 0;

  for (let i = 0; i < arr.length; i++) {
    const elementI = arr[i];

    for (let j = i; j < arr.length; j++) {
      const position = i + 1;
      const elementJ = arr[j];

      if(elementJ === position && elementJ !== elementI) {
        arr = arr.slice();
        [arr[i], arr[j]] = [arr[j], arr[i]];
        counter++;
      }
    }
  }
  return counter;

}

